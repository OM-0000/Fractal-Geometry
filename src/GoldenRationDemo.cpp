void GoldenRationDemo::DrawVogel(HDC hdc, HWND hwnd)
{
	RECT clirect;
	GetClientRect(hwnd, &clirect);
	POINT center;
	center.x = (clirect.right - clirect.left) / 2;
	center.y = (clirect.bottom - clirect.top) / 2;

	for (int i = numWindows - 1; i > -1; i--)
	{
		drawWindow(k[i], hdc, hwnd);
	}

	if (picWin.closed)
	{
		sli->drawPic = false;
	}


	DrawVogelEquation(center, hdc);

}

