void GoldenRationDemo::DrawVogelEquation(POINT origin, HDC memdc)
{
	updateVP();
	COLORREF red = 0x0000dd;
	COLORREF green = 0x00dd00;
	COLORREF blue = 0xdd0000;
	COLORREF colors[3] = { red,green, blue };

	float phi = (1 + sqrt(5)) / 2;
	float phi_squared = pow(phi, vp.constantTwo);
	for (int i = 1; i < vp.i_max; i++)
	{

		HPEN blkPen = CreatePen(PS_SOLID, 2, 0x00aaab);
		HBRUSH brush = CreateSolidBrush(0x00aaab);
		if (vp.colorson == 1)
		{
			DeleteObject(blkPen);
			DeleteObject(brush);
			blkPen = CreatePen(PS_SOLID, 2, colors[i % 3]);
			brush = CreateSolidBrush(colors[i % 3]);

		}

		SelectObject(memdc, blkPen);		
		SelectObject(memdc, brush);
		float theta = vp.constantOne * 3.14159 * i / phi_squared;
		float r = vp.scale * sqrt(i);
		POINT curr;
		int dx = r * cos(theta);
		int dy = r * sin(theta);
		curr.x = origin.x + dx;
		curr.y = origin.y + dy;
		RECT floret;
		int floret_radius = 10;
		floret.left = curr.x - floret_radius;
		floret.right = curr.x + floret_radius;
		floret.top = curr.y - floret_radius;
		floret.bottom = curr.y + floret_radius;
		Ellipse(memdc, floret.left, floret.top, floret.right, floret.bottom);
		DeleteObject(brush);
		DeleteObject(blkPen);
	}
}
